     public boolean consultar (String login, String senha){
// Manda como parametro para ele duas variaveis para ele procurar no banco de dados!
        try{
            boolean autentificado = false;
            String sql;
            sql = "select login, senha, funcao,nome, id from usuario where login = ? and senha = ?";
            PreparedStatement ps;
            BeansLogin l = new BeansLogin();
            ps = conexao.prepareStatement(sql);
            ps.setString(1, login);
            ps.setString(2, senha);
 
            ResultSet rs;
            rs = ps.executeQuery();
 
            if (rs.next()) {
                rs.getString("login");
                rs.getString("senha");
                rs.getString("funcao");
                l.setNome(rs.getString("nome"));
                BeansLogin.setId(rs.getInt("id"));
                autentificado = true;
            }
 
            ps.close();
   
 
            return autentificado;
 
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "ERRO Ao Recuperar Loguin ou Senha!\nErro: " + ex);
            throw new RuntimeException();
        }
}
     
