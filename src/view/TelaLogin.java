    public String consultaFunc(String login, String senha){
        try{
            String sql;
            sql = "select funcao from usuario where login = ? and senha = ?";
            PreparedStatement ps;
            String resposta = null;
            ps = conexao.prepareStatement(sql);
            ps.setString(1, login);
            ps.setString(2, senha);
 
            ResultSet rs;
            rs = ps.executeQuery();
            if(rs.next()){
                resposta = rs.getString("funcao");
            }
            
            return resposta;
            
            
        }catch (SQLException ex){
            JOptionPane.showMessageDialog(null, "ERRO Ao Recuperar Função!\nErro: " + ex);
            throw new RuntimeException();
        }
        
    }
